  var Utils = {};

  // Generate unique ID
  Utils.generateId = function() {
    return 'id_' + Date.now().toString(36) + '_' + Math.random().toString(36).substr(2, 9);
  };

  // Format currency (INR)
  Utils.formatCurrency = function(amount) {
    return '\u20B9' + Number(amount).toFixed(0);
  };

  // Sanitize HTML to prevent XSS
  Utils.sanitizeHTML = function(str) {
    var div = document.createElement('div');
    div.appendChild(document.createTextNode(str));
    return div.innerHTML;
  };

  // Debounce
  Utils.debounce = function(fn, delay) {
    var timer;
    return function() {
      var context = this;
      var args = arguments;
      clearTimeout(timer);
      timer = setTimeout(function() {
        fn.apply(context, args);
      }, delay);
    };
  };

  // Get URL params
  Utils.getUrlParam = function(key) {
    var params = new URLSearchParams(window.location.search);
    return params.get(key);
  };

  // Format date
  Utils.formatDate = function(dateStr) {
    var d = new Date(dateStr);
    var options = { day: 'numeric', month: 'short', year: 'numeric', hour: '2-digit', minute: '2-digit' };
    return d.toLocaleDateString('en-IN', options);
  };

  // Highlight search text
  Utils.highlightText = function(text, query) {
    if (!query) return Utils.sanitizeHTML(text);
    var escaped = query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    var regex = new RegExp('(' + escaped + ')', 'gi');
    return Utils.sanitizeHTML(text).replace(regex, '<span class="highlight">$1</span>');
  };

  // Toast notification
  Utils.showToast = function(message, type) {
    type = type || 'info';
    var container = document.getElementById('toast-container');
    if (!container) {
      container = document.createElement('div');
      container.id = 'toast-container';
      container.className = 'toast-container';
      document.body.appendChild(container);
    }

    var toast = document.createElement('div');
    toast.className = 'toast toast--' + type;
    toast.textContent = message;
    container.appendChild(toast);

    setTimeout(function() {
      if (toast.parentNode) toast.parentNode.removeChild(toast);
    }, 2500);
  };

